// ==================== HELPER COMPONENT ====================

/**
 * Rend l'icône appropriée pour un template donné
 */
interface TemplateIconRendererProps {
  iconType: string;
  className?: string;
}

export function TemplateIconRenderer({ iconType, className = "w-5 h-5" }: TemplateIconRendererProps) {
  switch (iconType) {
    case 'summarize':
      return <SummarizeIcon className={className} />;
    case 'translate':
      return <TranslateIcon className={className} />;
    case 'translate-french':
      return <TranslateFrenchIcon className={className} />;
    case 'spell-check':
      return <SpellCheckIcon className={className} />;
    case 'lightbulb':
      return <LightBulbIcon className={className} />;
    case 'code-review':
      return <CodeReviewIcon className={className} />;
    case 'sparkles':
      return <SparklesIcon className={className} />;
    case 'book-open':
      return <BookOpenIcon className={className} />;
    case 'book-stack':
      return <BookStackIcon className={className} />;
    case 'bug':
      return <BugIcon className={className} />;
    case 'photograph':
      return <PhotographIcon className={className} />;
    case 'color-swatch':
      return <ColorSwatchIcon className={className} />;
    case 'document-text':
      return <DocumentTextIcon className={className} />;
    case 'exclamation':
      return <ExclamationIcon className={className} />;
    case 'wand':
      return <WandIcon className={className} />;
    // Categories
    case 'pin':
      return <PinIcon className={className} />;
    case 'document':
      return <DocumentIcon className={className} />;
    case 'code':
      return <CodeIcon className={className} />;
    case 'globe':
      return <GlobeIcon className={className} />;
    case 'image':
      return <ImageIcon className={className} />;
    case 'link':
      return <LinkIcon className={className} />;
    case 'camera':
      return <CameraIcon className={className} />;
    case 'clipboard':
      return <ClipboardIcon className={className} />;
    // Notes
    case 'note':
      return <NoteIcon className={className} />;
    case 'tag':
      return <TagIcon className={className} />;
    case 'markdown':
      return <MarkdownIcon className={className} />;
    case 'download':
      return <DownloadIcon className={className} />;
    case 'plus':
      return <PlusIcon className={className} />;
    case 'trash':
      return <TrashIcon className={className} />;
    case 'clock':
      return <ClockIcon className={className} />;
    case 'checklist':
      return <ChecklistIcon className={className} />;
    case 'calendar':
      return <CalendarIcon className={className} />;
    case 'edit':
      return <EditIcon className={className} />;
    default:
      return <DocumentIcon className={className} />;
  }
}
